void get_chipid()
{
    ISoftwareI2CRegRead(&ovp921_i2c, OVP921_SCCB_ADDRESS_READ, ovp921.chipid.addr,
                        REG_ADDR_2BYTE, (uint8_t *)&ovp921.chipid.byte, 1, SCCB_DELAY_TIME);

    ISoftwareI2CRegRead(&ovp921_i2c, OVP921_SCCB_ADDRESS_READ, ovp921.chipid2.addr,
                        REG_ADDR_2BYTE, (uint8_t *)&ovp921.chipid2.byte, 1, SCCB_DELAY_TIME);
}

void off_pattern()
{
    ovp921.pattern_generator.pattern_generator_en = 0;
    ISoftwareI2CRegWrite(&ovp921_i2c, OVP921_SCCB_ADDRESS_WRITE, ovp921.pattern_generator.addr,
                         REG_ADDR_2BYTE, (uint8_t *)&ovp921.pattern_generator.byte, 1, SCCB_DELAY_TIME);
    change_anf(1);
}

uint8_t get_reg(uint16_t reg_addr)
{
    uint8_t reg_val = 0;
    ISoftwareI2CRegRead(&ovp921_i2c, OVP921_SCCB_ADDRESS_READ, reg_addr,
                        REG_ADDR_2BYTE, (uint8_t *)&reg_val, 1, SCCB_DELAY_TIME);
    DelayUs(400);
    return reg_val;
}

void set_reg(uint16_t reg_addr, uint8_t reg_val)
{
    ISoftwareI2CRegWrite(&ovp921_i2c, OVP921_SCCB_ADDRESS_WRITE, reg_addr,
                         REG_ADDR_2BYTE, (uint8_t *)&reg_val, 1, SCCB_DELAY_TIME);
    DelayUs(400);
}

void omnivision_lcos_init()
{
    delay_1ms(5);
    gpio_bit_reset(OVP921_RESET_PORT, OVP921_RESET_PIN);
}

void vertical_flip(bool enable)
{
    // read vertical flip status
#if 1
    ovp921.microlcd_serial_port_address_low.dp_addr = 0x06;
    ovp921.microlcd_serial_port_address_high.read_request = 1;
    set_reg(ovp921.microlcd_serial_port_address_low.addr, ovp921.microlcd_serial_port_address_low.byte);
    set_reg(ovp921.microlcd_serial_port_address_high.addr, ovp921.microlcd_serial_port_address_high.byte);

    while ((0x80 & get_reg(ovp921.microlcd_serial_port_address_high.addr)) != 0x00)
    {
        vTaskDelay(1);
    }

    uint8_t dp_data = get_reg(ovp921.microlcd_serial_port_data.addr);
#endif

    ovp921.microlcd_serial_port_address_low.dp_addr = 0x06;
    ovp921.microlcd_serial_port_address_high.byte = 0x00;

    if (enable)
    {
        set_reg(ovp921.microlcd_serial_port_address_low.addr, ovp921.microlcd_serial_port_address_low.byte);
        set_reg(ovp921.microlcd_serial_port_address_high.addr, ovp921.microlcd_serial_port_address_high.byte);
        set_reg(ovp921.microlcd_serial_port_data.addr, dp_data | 0x40);
    }
    else
    {
        set_reg(ovp921.microlcd_serial_port_address_low.addr, ovp921.microlcd_serial_port_address_low.byte);
        set_reg(ovp921.microlcd_serial_port_address_high.addr, ovp921.microlcd_serial_port_address_high.byte);
        set_reg(ovp921.microlcd_serial_port_data.addr, dp_data & (uint8_t)(~0x40));
    }
}

void horizontal_flip(bool enable)
{
    ovp921.horizontal_mode.horizontal_filp = enable;
    set_reg(ovp921.horizontal_mode.addr, (uint8_t)ovp921.horizontal_mode.byte);
}

bool get_ovp921_status()
{
    if (get_reg(0x004C) == 0x17)
    {
        return true;
    }
    else
    {
        return false;
    }
}

void halting_internal_mcu()
{
    set_reg(0x01ff, 0x05);
    set_reg(0x7ffe, 0x01);
    set_reg(0x8000, 0x00);
    set_reg(0x8001, 0x75);
    set_reg(0x8002, 0x87);
    set_reg(0x8003, 0x02);
    set_reg(0x8004, 0x00);
    set_reg(0x8005, 0x00);
    set_reg(0x7ffe, 0x00);
    set_reg(0x7fc0, 0x03);
}
#define RETRY_TIMES 10
bool ovp921_erase(uint32_t addr) // erase one page
{

    set_reg(0x0210, 0x02);
    vTaskDelay(5 + 5);
    set_reg(0x0210, 0x03);

    int times = 0;
    while (get_reg(0x021d) != 0x00 && times < RETRY_TIMES)
    {
        times++;
        vTaskDelay(5 + 5);
    }
    if (times >= RETRY_TIMES)
    {
        return false;
    }

    set_reg(0x0213, 0x00);
    set_reg(0x0218, 0x06);
    vTaskDelay(15 + 5);
    set_reg(0x0213, 0x01);
    set_reg(0x0218, 0x01);
    set_reg(0x0218, 0x00);
    vTaskDelay(30 + 5);
    set_reg(0x0213, 0x00);
    set_reg(0x0218, 0x06);
    vTaskDelay(15 + 5);

    uint8_t read_fifo_times = 0;
    read_fifo_times = get_reg(0x021e);
    for (uint8_t i = 0; i < read_fifo_times; i++)
    {
        get_reg(0x0218);
    }

    set_reg(0x0213, 0x04);
    set_reg(0x0218, 0x20);

    set_reg(0x0218, (uint8_t)((addr >> 16) & 0xFF));
    set_reg(0x0218, (uint8_t)((addr >> 8) & 0xFF));
    set_reg(0x0218, (uint8_t)(addr & 0xFF));
    vTaskDelay(300 + 25);

    set_reg(0x0213, 0x00);
    set_reg(0x0218, 0x00);

    vTaskDelay(300 + 5);

    return true;
}

#define FLASH_PAGE_SIZE 0x100
void ovp921_write_page(uint32_t addr, const uint8_t *data)
{
    set_reg(0x0213, 0x00);
    set_reg(0x0218, 0x06);
    vTaskDelay(105 + 5);
    set_reg(0x0213, 0x01);
    set_reg(0x0218, 0x05);
    set_reg(0x0218, 0x00);
    uint8_t read_fifo_times = 0;

    read_fifo_times = get_reg(0x021e);
    for (uint8_t i = 0; i < read_fifo_times; i++)
    {
        get_reg(0x0218);
    }

    set_reg(0x0213, 0x04);
    set_reg(0x0218, 0x02);

    set_reg(0x0218, (uint8_t)((addr >> 16) & 0xFF));
    set_reg(0x0218, (uint8_t)((addr >> 8) & 0xFF));
    set_reg(0x0218, (uint8_t)(addr & 0xFF));

    for (size_t i = 0; i < FLASH_PAGE_SIZE; i++)
    {
        set_reg(0x0218, *(data + i));
        get_reg(0x0218);
    }
    set_reg(0x0213, 0x00);
    set_reg(0x0218, 0x00);
    vTaskDelay(150 + 10);
}

